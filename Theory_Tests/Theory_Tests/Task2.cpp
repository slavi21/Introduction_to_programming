#include <iostream>

int main() {
    double b = 5;      // b = 5
    double& a = b;     // a е псевдоним на b
    double& c = a;     // c е псевдоним на a → пак на b

    --b;               // b става 4  (a и c „виждат“ 4)
    a += 3;            // добавяме 3 към същото място → b става 7
    std::cout << c;    // c е псевдоним на b → печата 7
    // a, b и c са три имена за едно и също място в паметта, затова всяка промяна през едно от тях се вижда през останалите.
}